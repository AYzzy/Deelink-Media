  const [formData, setFormData] = useState<{
    name: string;
    email: string;
    phone: string;
    eventType: string;
    location: string;
    date: string;
    services: string[];
    message: string;
  }>({
    name: '',
    email: '',
    phone: '',
    eventType: '',
    location: '',
    date: '',
    services: [],
    message: ''
  });

